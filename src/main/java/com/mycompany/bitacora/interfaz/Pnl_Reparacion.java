    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if(tblReparacion.getRowCount() > 0 ){
            
            if(tblReparacion.getSelectedRow()!=-1){
                int id_repa = Integer.parseInt(String.valueOf(tblReparacion.getValueAt(tblReparacion.getSelectedRow(),0)));
                
                Pnl_ModificarRepa miModifica = new Pnl_ModificarRepa(id_repa);
                miModifica.setSize(800, 600);
                miModifica.setLocation(0, 0);
        
                this.removeAll();
                this.add(miModifica,BorderLayout.CENTER);
                this.revalidate();
                this.repaint();
                
                
                
            }
            else{
            
                mensajes("Selecionar algun registro", "error", "Borrar registros");
            }
        }else{
        
            mensajes("Sin registros a eliminar", "error", "Borrar registros");
        }
        
        
        
        
        
        
    }//GEN-LAST:event_btnEditarActionPerformed

