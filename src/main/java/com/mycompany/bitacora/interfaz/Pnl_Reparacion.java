    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tblReparacion.getRowCount() > 0 ){
            
            if(tblReparacion.getSelectedRow()!=-1){
                
                int id_repa = Integer.parseInt(String.valueOf(tblReparacion.getValueAt(tblReparacion.getSelectedRow(),0)));
                
                miControl.borrarReparacion(id_repa);
                
                mensajes("Registro Eliminado", "info", "Borrar registros");
                
                cargarTabla();
                
            }
            else{
            
                mensajes("Selecionar algun registro", "error", "Borrar registros");
            }
        }
        else{
        
            mensajes("Sin registros a eliminar", "error", "Borrar registros");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed
    public void mensajes(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
        
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
            
        }else if(tipo.equals("error")){
            
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        } 
            
            JDialog dialogo = optionPane.createDialog(titulo);
            dialogo.setAlwaysOnTop(true);
            dialogo.setVisible(true);
    }
    

