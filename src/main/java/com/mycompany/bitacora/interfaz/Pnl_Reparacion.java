    private void cargarTabla() {
        //definir modelo de tabla
        DefaultTableModel tabla = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row, int column){
                 return false;
            }
        
        };
        
        //establecer nombres de las columnas
        String titulos[] = {"id","Calle", "PuntoV","Fecha","Estado","Nombre","Turno","Grupo","Descrip"};
            
        tabla.setColumnIdentifiers(titulos);
        
        //carga de datos
        List <Reparaciones> listaReparaciones = miControl.traerDatos();
        //recorrer la lista
        if (listaReparaciones != null) {
            
            for (Reparaciones repas : listaReparaciones) {
                
                Object[] objeto = {repas.getId_Reparaciones(),repas.getCalle(),repas.getPunto(),repas.getFecha(),repas.getEstado(),repas.getNombre(),repas.getTurno(),repas.getGrupo(),repas.getDescrip()};
                
                tabla.addRow(objeto);
            }
        }
        
        tblReparacion.setModel(tabla);
    }
